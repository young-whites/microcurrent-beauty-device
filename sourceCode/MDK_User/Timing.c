/*****************************************************************************
** 功能：	1毫秒定时
*****************************************************************************/
void Timing1_1ms(void)
{
	Flag.WorkStart = 1;
	LED_DrvScan();
	BEEP_DrvScan();
	APP_TimeOverScan();
}


/*****************************************************************************
** 功能：	10毫秒定时
*****************************************************************************/
void Timing1_10ms(void)
{
	KEY_DrvScan();
	/***********************************************************/
	/*                蓝牙工作状态扫描                         */
	/***********************************************************/
	if (Flag.PowerOn == 1)
	{
		Bluetooth_Scan();
	}
		
}



