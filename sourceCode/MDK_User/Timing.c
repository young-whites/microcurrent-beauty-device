/*****************************************************************************
** 功能：	100毫秒定时
*****************************************************************************/
void Timing1_100ms(void)
{
    /* Update cooling PID control */
    if (Flag.WorkStart && g_cooling_pid.enabled)
    {
        PID_Update();
        Cooling_SetPower(PID_GetOutput());
    }
}



