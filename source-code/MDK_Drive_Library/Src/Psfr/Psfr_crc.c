#include "cms32f033x.h"
#include "Psfr_crc.h"

/*****************************************************************************
 ** 功能：	CRC 输入需要运算的8 位数据
 ** 参数： [in] data
 ** 返回：  none
 ** 说明：	
*****************************************************************************/
void CRC_Input(uint32_t data)
{
	CRC->CRCIN = (uint8_t)data;
}

/*****************************************************************************
 ** 功能：	CRC 输出运算的16位结果
 ** 参数： [in] none
 ** 返回： 16 bits Result
 ** 说明：	
*****************************************************************************/
uint32_t  CRC_Output(void)
{
	return((uint16_t)CRC->CRCD);
}
