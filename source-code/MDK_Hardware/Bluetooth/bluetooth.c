#include "bluetooth.h"



//----------------------------------------------------------------------------
INT8U	btPowerOnCnt = 0xFF;						// РЖбРЕчдДПЊЦєМЦЪБ
static	INT8U	bBtCnct = 0;						// РЖбРСЌНгзДЬЌ
//----------------------------------------------------------------------------



/*****************************************************************************
* ЙІФм:		РЖбРФЃПщ ЖЫПкГѕЪМЛЏ
*****************************************************************************/
void Bluetooth_GPIO_Config(void)
{
	// P36ЙмНХХфжУ (BLE_INT/MCU_DET_BT_STATUS)
	SYS_SET_IOCFG(IOP36CFG, SYS_IOCFG_P36_GPIO);						
	GPIO_CONFIG_IO_MODE(GPIO3, GPIO_PIN_6, GPIO_MODE_INPUT_PULL_DOWN);	
	GPIO_DisableInt(GPIO3, GPIO_PIN_6_MSK);								

	// P26ЙмНХХфжУ (BLE_VCC_BLE)
	SYS_SET_IOCFG(IOP26CFG, SYS_IOCFG_P26_GPIO);							
	GPIO_CONFIG_IO_MODE(GPIO2, GPIO_PIN_6, GPIO_MODE_OUTPUT_PUSH_PULL);	
	GPIO_DisableInt(GPIO2, GPIO_PIN_6_MSK);								

	Bluetooth_PowerOff();
}




/*****************************************************************************
* ЙІФм:		РЖбРФЃПщ ЩЈУш ЃЈ10msЩЈУшвЛДЮЃЉ
*****************************************************************************/
void Bluetooth_Scan(void)
{
	static	int8_t	send = 1;
	static	int8_t	cnt = 0;
	int8_t	curr;

	if (btPowerOnCnt < 0xFF) {								// ЪзДЮЩЯЕчПЊЛњКѓ0.5УыЃЌЩшжУвЛДЮУћГЦ
		btPowerOnCnt++;
		if (send && (btPowerOnCnt == 50)) {
			send = 0;
			UART1_SendString("AT+NAMEDr-05\r\n");
		}
	}

	curr = Bluetooth_GetState();							// ЛёШЁРЖбРФЃПщЕФСЌНгзДЬЌ
	if (curr != bBtCnct)
	{
		/* СЌај10ДЮзДЬЌИФБф ВХШЯЮЊецЕФИФБф */
		if (++cnt >= 10)
		{
			cnt = 0;
			bBtCnct = curr;									// БЃДцаТЕФРЖбРСЌНгзДЬЌ
			if (bBtCnct)
			{
				BEEP_Blink(2,0,0);							// РЖбРИеСЊЭЈЪБЃЌЗфУљЦїУљНа2Щљ
			}
		}
	}
	else 
	{
		cnt = 0;
	}
}

/*****************************************************************************
* ЙІФм:		ЛёШЁРЖбРФЃПщЕФСЌНгзДЬЌ
* ЗЕЛи:		0ЃКЮДСЌНгЃЛ1ЃКвбСЌНг
*****************************************************************************/
INT8U Bluetooth_GetConnect(void)
{
	return bBtCnct;
}



/*****************************************************************************
* ЙІФм:		ЛёШЁРЖбРФЃПщЕФСЌНгзДЬЌ
* ЗЕЛи:		0ЃКЮДСЌНгЃЛ1ЃКвбСЌНг
*****************************************************************************/
INT8U Bluetooth_GetState(void)
{
	return GPIO_GetPin(GPIO3, GPIO_PIN_6_MSK) ? 1 : 0;
}




/*****************************************************************************
* ЙІФм:		РЖбРФЃПщЕчдДПЊЦє
*****************************************************************************/
void Bluetooth_PowerOn(void)
{
	GPIO_SetPin(GPIO2, GPIO_PIN_6_MSK);					
}



/*****************************************************************************
* ЙІФм:		РЖбРФЃПщЕчдДЧаЖЯ
*****************************************************************************/
void Bluetooth_PowerOff(void)
{
	GPIO_ResetPin(GPIO2, GPIO_PIN_6_MSK);		
}

