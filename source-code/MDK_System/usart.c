/*****************************************************************************
* 功能:		获取FIFO缓冲区中数据的个数
* 返回:		数量
*****************************************************************************/
INT8U FIFO_GetCnt(FIFO_TypeDef* fifo)
{
	return (fifo->Writ - fifo->Read);
}

/*****************************************************************************
* 功能:		检查FIFO缓冲区是否为空
* 返回:		1表示空，0表示非空。
*****************************************************************************/
INT8U FIFO_IsEmpty(FIFO_TypeDef* fifo)
{
	if (fifo->Writ == fifo->Read)	return 1;
	else							return 0;
}

/*****************************************************************************
* 功能:		检查FIFO缓冲区是否已满
* 返回:		1表示已满，0表示未满。
*****************************************************************************/
INT8U FIFO_IsFull(FIFO_TypeDef* fifo)
{
	if ((fifo->Writ + 1) % fifo->Size == fifo->Read)	return 1;
	else										return 0;
}

/*****************************************************************************
* 功能:		写入一个数据到FIFO缓冲区
* 返回:		1表示成功，0表示失败（缓冲区已满）。
*****************************************************************************/
INT8U FIFO_In(FIFO_TypeDef* fifo, INT8U dat)
{
	if (FIFO_IsFull(fifo))	return 0;			// FIFO已满，失败返回
	fifo->Addr[fifo->Writ] = dat;				// 数据写入FIFO
	fifo->Writ += 1;							// 调整写入位置
	fifo->Writ %= fifo->Size;					// 位置卷绕
	return 1;									// 成功返回
}

/*****************************************************************************
* 功能:		从FIFO缓冲区读出一个数据
* 返回:		1表示成功，0表示失败（缓冲区已空）。
*****************************************************************************/
INT8U FIFO_Out(FIFO_TypeDef* fifo, INT8U* dat)
{
	if (FIFO_IsEmpty(fifo))	return 0;			// FIFO已空，失败返回
	*dat = fifo->Addr[fifo->Read];				// 数据读出
	fifo->Read += 1;							// 调整读出位置
	fifo->Read %= fifo->Size;					// 位置卷绕
	return 1;									// 成功返回
}
