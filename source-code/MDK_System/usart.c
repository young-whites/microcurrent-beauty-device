/*****************************************************************************
* 功能:		UART1初始化
* 参数:		baud:	波特率（单位：bps）
*****************************************************************************/
void UART1_Init(INT32U baud)
{

#if (UART1_SEND_ENB)
	_txBusy = 0;								// 置发送模块为空闲
	UART1_SetFifoTxD(_txd_buff, BUF_SIZE);
	UART_DisableTxdInt(UART1);
#endif	

#if (UART1_RECV_ENB)

	UART1_SetFifoRxD(_rxd_buff, BUF_SIZE);
	UART_GetIntFlag(UART1);
	UART_EnableRxdInt(UART1);
#endif	

	UART_ConfigRunMode(UART1, baud, UART_WLS_8, UART_PARITY_NONE, UART_STOP_BIT_1);
	NVIC_EnableIRQ(UART1_IRQn);
	NVIC_SetPriority(UART1_IRQn, 0);
	SYS_EnablePeripheralClk(SYS_CLK_UART1_MSK);
}






